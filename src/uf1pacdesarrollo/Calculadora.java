    /*metodo con switch case que según el botón de operación que se pulse
    realiza la operación*/
    public void operacion() {

        switch (calculo) {

            /*recoge el String que se ha escrito en el jTextField1, lo parsea
            a Double realiza la operación marcada. Lo asigna a la variable
            respuesta y se muestra en la pantalla. En el caso de que respuesta 
            tenga más de 14 decimales, lo redondea para que quepa en pantalla*/
            case 1: //suma
                respuesta = numero + Double.parseDouble(jTextField1.getText());
                jLabel1.setText(textoOperacion + jTextField1.getText());
                respuesta = Math.round(respuesta * Math.pow(10, 14)) / Math.pow(10, 14);
                jTextField1.setText(Double.toString(respuesta));
                puntoDecimal = 0;
                primerNumero = 0;
                break;

            case 2: //resta
                respuesta = numero - Double.parseDouble(jTextField1.getText());
                jLabel1.setText(textoOperacion + jTextField1.getText());
                respuesta = Math.round(respuesta * Math.pow(10, 14)) / Math.pow(10, 14);
                jTextField1.setText(Double.toString(respuesta));
                puntoDecimal = 0;
                primerNumero = 0;
                break;

            case 3: //multiplicación
                respuesta = numero * Double.parseDouble(jTextField1.getText());
                jLabel1.setText(textoOperacion + jTextField1.getText());
                respuesta = Math.round(respuesta * Math.pow(10, 14)) / Math.pow(10, 14);
                jTextField1.setText(Double.toString(respuesta));
                puntoDecimal = 0;
                primerNumero = 0;
                break;

            case 4: //división
                if (Double.parseDouble(jTextField1.getText()) == 0) {
                    jLabel1.setText("No puedes dividir por 0");
                } else {
                    respuesta = numero / Double.parseDouble(jTextField1.getText());
                }
                jLabel1.setText(textoOperacion + jTextField1.getText());
                respuesta = Math.round(respuesta * Math.pow(10, 14)) / Math.pow(10, 14);
                jTextField1.setText(Double.toString(respuesta));
                puntoDecimal = 0;
                primerNumero = 0;
                break;

            case 5: //elevado al cuadrado
                respuesta = Math.pow(numero, 2);
                jLabel1.setText(textoOperacion + " ^2 =");
                respuesta = Math.round(respuesta * Math.pow(10, 14)) / Math.pow(10, 14);
                jTextField1.setText(Double.toString(respuesta));
                puntoDecimal = 0;

                break;

        }
    }


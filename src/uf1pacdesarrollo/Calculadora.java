    /*al pulsar los botones de las operaciones aritméticas se almacena en la
    variable numero el String que hay en pantalla y se parsea a Double. Se indica
    cual de los case  corresponde y se deja la pantalla 
    en blanco en espera del siguiente número*/
    private void jBSumarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSumarActionPerformed

        if (jTextField1.getText().equals("")) {
            jLabel1.setText("Primero escribe los números");
        } else {
            numero = Double.parseDouble(jTextField1.getText());
            calculo = 1;
            textoOperacion = jTextField1.getText() + " + ";
            jLabel1.setText(textoOperacion);
            jTextField1.setText("");
            primerNumero = 1;
        }

    }//GEN-LAST:event_jBSumarActionPerformed


    /*El botón de borrar elimina el último carácter escrito. Se pasa lo que hay
    en el jTextField a la veriable numPant (String) y si la longitud de esa 
    variable es mayor que 0 elimina el último caracter utilizando substring*/
    private void jBBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBorrarActionPerformed

        String numPant = jTextField1.getText().toString();
        if (numPant.length() > 0) {
            numPant = numPant.substring(0, numPant.length() - 1);
            jTextField1.setText(numPant);
        }
    }//GEN-LAST:event_jBBorrarActionPerformed
